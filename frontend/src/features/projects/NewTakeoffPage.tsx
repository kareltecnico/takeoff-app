function generateProjectCode(projectName: string) {
  const stem =
    projectName
      .trim()
      .toUpperCase()
      .replace(/[^A-Z0-9]+/g, '')
      .slice(0, 6) || 'PRJ'
  const random = crypto.randomUUID().split('-')[0].toUpperCase()
  return `${stem}-${random}`
}

function toNumber(value: string) {
  const parsed = Number(value)
  return Number.isFinite(parsed) ? parsed : 0
}

function numberString(value: number) {
  return Number.isInteger(value) ? String(value) : value.toFixed(1)
}

function formatCurrency(value: number) {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
  }).format(value)
}

function taxLabel(taxable: boolean) {
  return taxable ? '7%' : '0%'
}

function buildInitialSelections(templateCode: keyof typeof BASELINE_DEFAULT_ITEMS | null): StandardItemSelections {
  const fallback = Object.fromEntries(
    Object.entries(STANDARD_ITEM_OPTIONS).map(([key, options]) => [key, options[0]?.code ?? '']),
  ) as StandardItemSelections

  if (!templateCode) {
    return fallback
  }

  return {
    ...fallback,
    ...BASELINE_DEFAULT_ITEMS[templateCode],
  }
}

function parseModelEntries(input: string) {
  return input
    .split(/[\n,]+/)
    .map((entry) => entry.trim())
    .filter(Boolean)
}

function itemDisplayLabel(item: Pick<CatalogItem, 'description' | 'details'>) {
  return item.details && item.details !== '0' ? `${item.description} — ${item.details}` : item.description
}

function stageLabel(stage: Stage) {
  if (stage === 'topout') return 'TopOut'
  if (stage === 'ground') return 'Ground'
  return 'Final'
}

function normalizeCatalogSearch(item: Pick<CatalogItem, 'code' | 'description' | 'details'>) {
  return `${item.code} ${item.description} ${item.details ?? ''}`.toUpperCase()
}

function matchesOperatorCategory(item: CatalogItem, category: string) {
  const search = normalizeCatalogSearch(item)

  switch (category) {
    case 'Kitchen Faucet':
      return (
        search.includes('KITCHEN FAUCET') ||
        search.includes('KITCH,FAUCET') ||
        item.code.startsWith('XPLBFMKF') ||
        item.code.startsWith('FW50MK') ||
        item.code.startsWith('KITFAU')
      )
    case 'Lav Faucet':
      return (
        search.includes('LAVATORY FAUCET') ||
        search.includes('LAV,FAUCET') ||
        search.includes('LAV,FAUCT') ||
        item.code.startsWith('XPLBFMLF') ||
        item.code.startsWith('FW50ML')
      )
    case 'Shower Trim':
      return (
        (search.includes('SHWR,TRIM') || search.includes('SHOWER TRIM')) &&
        !search.includes('TUB/SHWR') &&
        !search.includes('HEAD') &&
        !search.includes('ARM') &&
        !search.includes('DROP ELL') &&
        !search.includes('HOSE') &&
        !search.includes('BRACKET')
      )
    case 'Tub Shower Trim':
      return search.includes('TUB/SHWR,TRIM') || item.code.startsWith('XPLBFMTST')
    case 'Bathtub':
      return (
        item.code.startsWith('XPLBFTBA') ||
        item.code.startsWith('XPLBFTBC') ||
        item.code.startsWith('XPLBFTBD') ||
        search.includes('BATHTUB') ||
        search.includes('TUB,ALCOVE')
      )
    case 'Pedestal':
      return search.includes('PEDESTAL') || search.includes('LAV,PED') || item.code.startsWith('XPLBF0LP')
    case 'Toilet':
      return item.code.startsWith('XPLBF0TC') || search.startsWith('TOILET')
    case 'Tank Water Heater':
      return (
        (search.includes('WATER HEATER') || search.includes('W HEATER')) &&
        search.includes('TANK') &&
        !search.includes('TANKLESS') &&
        !search.includes('INSTALL')
      )
    case 'Tankless Water Heater':
      return (
        (search.includes('TANKLESS') || item.code.startsWith('XPLBFWHL')) &&
        !search.includes('INSTALL')
      )
    default:
      return false
  }
}

